async def _has_links(user_idea):
    """–§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –Ω–∞–ª–∏—á–∏—è —Å—Å—ã–ª–æ–∫ –≤ –∏–¥–µ–∏(—Ç–µ–∫—Å—Ç–µ) –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
